// Program that from n to m and prints the numbers divisible by divisor
// COMP1511 H13C

#include <stdio.h>

int main(void) {
    // prompt the user
    int start, finish, divisor;
    printf("Enter start: ");
    scanf("%d", &start);
    
    printf("Enter finish: ");
    scanf("%d", &finish);

    printf("Enter divisor: ");
    scanf("%d", &divisor);
        
    // print output

    while (start <= finish) {
        // print if divisible
        if (start % divisor == 0) {
            printf("%d\n", start);
        }
        
        
        start = start + 1;
    }
    
    return 0;
}
